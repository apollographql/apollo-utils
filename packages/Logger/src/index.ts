/**
 * This interface heavily borrows from the `ts-log` package. However, the `trace`
 * function is not on all of the common loggers that we claim to support so
 * it's been removed in this copy of this interface.
 */

/**
 * Represents a generic logger that could be a simple console, bunyan etc.
 */
 export interface Logger {
  debug(message?: any, ...optionalParams: any[]): void;
  info(message?: any, ...optionalParams: any[]): void;
  warn(message?: any, ...optionalParams: any[]): void;
  error(message?: any, ...optionalParams: any[]): void;
  [x: string]: any;
}
